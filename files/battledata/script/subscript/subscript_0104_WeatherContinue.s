    .include "macros/btlcmd.inc"

    .data

_000:
    Call BATTLE_SUBSCRIPT_SHOW_PREPARED_MESSAGE
    PlayBattleAnimationFromVar BATTLER_CATEGORY_PLAYER, BSCRIPT_VAR_TEMP_DATA
    Wait 
    UpdateVar OPCODE_SET, BSCRIPT_VAR_BATTLER_SPEED_TEMP, 0

_009:
    GetMonBySpeedOrder BSCRIPT_VAR_MSG_BATTLER_TEMP
    EndOfTurnWeatherEffect BATTLER_CATEGORY_MSG_TEMP
    CompareVarToValue OPCODE_EQU, BSCRIPT_VAR_TEMP_DATA, 1, _098
    CompareVarToValue OPCODE_EQU, BSCRIPT_VAR_HP_CALC, 0, _090
    CompareVarToValue OPCODE_GT, BSCRIPT_VAR_HP_CALC, 0, _076
    CheckAbility CHECK_OPCODE_HAVE, BATTLER_CATEGORY_MSG_TEMP, ABILITY_MAGIC_GUARD, _090
    CompareVarToValue OPCODE_FLAG_SET, BSCRIPT_VAR_FIELD_CONDITION, FIELD_CONDITION_SUN_ALL, _045
    // {1} is buffeted by the {0}!
    PrintMessage msg_0197_00285, TAG_MOVE_NICKNAME, BATTLER_CATEGORY_MSG_TEMP, BATTLER_CATEGORY_MSG_TEMP
    GoTo _062

_045:
    CompareVarToValue OPCODE_EQU, BSCRIPT_VAR_TEMP_DATA, 2, _057
    // {0} is hurt by its {1}!
    PrintMessage msg_0197_01090, TAG_NICKNAME_ABILITY, BATTLER_CATEGORY_MSG_TEMP, BATTLER_CATEGORY_MSG_BATTLER_TEMP
    GoTo _062

_057:
    // {0} lost some HP because of its {1}!
    PrintMessage msg_0197_01096, TAG_NICKNAME_ABILITY, BATTLER_CATEGORY_MSG_TEMP, BATTLER_CATEGORY_MSG_BATTLER_TEMP

_062:
    Wait 
    WaitButtonABTime 30
    Call BATTLE_SUBSCRIPT_UPDATE_HP
    CompareVarToValue OPCODE_FLAG_NOT, BSCRIPT_VAR_BATTLE_STATUS, BATTLE_STATUS_FAINTED, _090
    Call BATTLE_SUBSCRIPT_FAINT_MON
    GoTo _090

_076:
    UpdateVar OPCODE_FLAG_ON, BSCRIPT_VAR_BATTLE_STATUS, BATTLE_STATUS_NO_BLINK
    Call BATTLE_SUBSCRIPT_UPDATE_HP
    // {0} restored HP using its {1}!
    PrintMessage msg_0197_00635, TAG_NICKNAME_ABILITY, BATTLER_CATEGORY_MSG_TEMP, BATTLER_CATEGORY_MSG_BATTLER_TEMP
    Wait 
    WaitButtonABTime 30

_090:
    UpdateVar OPCODE_ADD, BSCRIPT_VAR_BATTLER_SPEED_TEMP, 1
    GoToIfValidMon BSCRIPT_VAR_BATTLER_SPEED_TEMP, _009
    End 

_098:
    Call BATTLE_SUBSCRIPT_ABILITY_RESTORE_STATUS
    GoTo _090
